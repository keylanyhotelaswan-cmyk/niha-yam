/**
 * Writes `.env.testing` from the Supabase CLI session for project Testing.
 * Also ensures a database password is available for pooler-based migrations
 * (direct db.* host is IPv6-only).
 *
 * Usage: node scripts/write-testing-env.mjs
 */
import { randomBytes } from 'node:crypto'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { homedir } from 'node:os'
import { resolve } from 'node:path'
import { execFileSync } from 'node:child_process'
import { TESTING_SUPABASE_REF, loadEnvFile } from './load-env.mjs'

function parseEnvOutput(text) {
  const out = {}
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#') || !trimmed.includes('=')) continue
    const i = trimmed.indexOf('=')
    const key = trimmed.slice(0, i).trim()
    let value = trimmed.slice(i + 1).trim()
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1)
    }
    out[key] = value
  }
  return out
}

function findAccessToken() {
  const candidates = [
    resolve(homedir(), '.supabase', 'access-token'),
    resolve(process.env.APPDATA || '', 'supabase', 'access-token'),
    resolve(process.env.LOCALAPPDATA || '', 'supabase', 'access-token'),
  ]
  for (const p of candidates) {
    if (p && existsSync(p)) {
      const token = readFileSync(p, 'utf8').trim()
      if (token) return token
    }
  }
  return null
}

async function ensureDbPassword(existingPassword) {
  if (existingPassword) return { password: existingPassword, source: 'existing' }

  const token = findAccessToken()
  if (!token) {
    return { password: null, source: 'missing-token' }
  }

  const password = `NihaTest_${randomBytes(12).toString('base64url')}`
  const res = await fetch(
    `https://api.supabase.com/v1/projects/${TESTING_SUPABASE_REF}/database/password`,
    {
      method: 'PUT',
      headers: {
        Authorization: `Bearer ${token}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ password }),
    },
  )

  if (!res.ok) {
    const text = await res.text().catch(() => '')
    return {
      password: null,
      source: `api-${res.status}`,
      detail: text.slice(0, 200),
    }
  }

  return { password, source: 'api-reset' }
}

async function main() {
  const raw = execFileSync(
    'supabase',
    ['projects', 'api-keys', '--project-ref', TESTING_SUPABASE_REF, '-o', 'env'],
    { encoding: 'utf8', shell: true },
  )
  const keys = parseEnvOutput(raw)
  const anon =
    keys.SUPABASE_ANON_KEY || keys.ANON_KEY || keys.VITE_SUPABASE_ANON_KEY
  const service =
    keys.SUPABASE_SERVICE_ROLE_KEY ||
    keys.SERVICE_ROLE_KEY ||
    keys.SUPABASE_DEFAULT_KEY

  if (!anon || !service) {
    console.error(
      'FAIL: Could not resolve anon/service keys from supabase CLI for Testing project.',
    )
    process.exit(1)
  }

  const dest = resolve(process.cwd(), '.env.testing')
  let previous = {}
  if (existsSync(dest)) {
    try {
      previous = loadEnvFile(dest)
    } catch {
      previous = {}
    }
  }

  const dbResult = await ensureDbPassword(previous.SUPABASE_DB_PASSWORD)
  if (!dbResult.password) {
    console.warn(
      'WARN: Could not auto-set SUPABASE_DB_PASSWORD (' +
        dbResult.source +
        '). Add it manually from Dashboard → Database.',
    )
    if (dbResult.detail) console.warn('  detail:', dbResult.detail)
  } else if (dbResult.source === 'api-reset') {
    console.log('OK: database password reset for Testing (saved to .env.testing)')
  }

  const url = `https://${TESTING_SUPABASE_REF}.supabase.co`
  const body = `# NIHA Testing environment — DO NOT use for production
# Generated by scripts/write-testing-env.mjs — gitignored
# Project: ${TESTING_SUPABASE_REF}

VITE_APP_ENV=testing
VITE_SUPABASE_URL=${url}
VITE_SUPABASE_ANON_KEY=${anon}

# Server-side scripts only (seed / bootstrap) — never bundle in frontend
SUPABASE_SERVICE_ROLE_KEY=${service}

# Postgres password for migrate:testing (IPv4 pooler — required on Windows)
${dbResult.password ? `SUPABASE_DB_PASSWORD=${dbResult.password}` : '# SUPABASE_DB_PASSWORD='}

# Fixed seed logins (see docs/testing-environment.md)
TESTING_MANAGER_USERNAME=manager
TESTING_MANAGER_PASSWORD=Testing123!
TESTING_MANAGER_PIN=1111
TESTING_CASHIER_USERNAME=cashier
TESTING_CASHIER_PASSWORD=Testing123!
TESTING_CASHIER_PIN=2222
`

  writeFileSync(dest, body, 'utf8')
  console.log('OK: wrote .env.testing for project', TESTING_SUPABASE_REF)
  console.log('  url:', url)
  console.log('  anon key: set')
  console.log('  service role: set')
  console.log(
    '  db password:',
    dbResult.password ? `set (${dbResult.source})` : 'MISSING — add manually',
  )
}

main().catch((error) => {
  console.error('FAIL:', error instanceof Error ? error.message : error)
  process.exit(1)
})
